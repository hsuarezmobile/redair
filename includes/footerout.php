		   url : 'doLogin.php',
		   type : 'POST',
		   data : formData,
		   processData: false,
		   contentType: false,
		   success : function(datax) {
				var myObj = JSON.parse(datax);
				if (myObj.result){
					document.getElementById("loginButton").style.display = "none";
